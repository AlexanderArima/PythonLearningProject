# if语句
alien_color = 'red'
if alien_color == 'green':
    print('你获得了5分')
    
alien_color = 'green'
if alien_color == 'green':
    print('你获得了5分')

# if-else语句
alien_color = 'red'
if alien_color == 'green':
    print('你获得了5分')
else:
    print('你获得了10分')

# if-elif-elif语句
alien_color = 'red'
if alien_color == 'green':
    print('你获得了5分')
elif alien_color == 'yellow':
    print('你获得了10分')
elif alien_color == 'red':
    print('你获得了15分')

